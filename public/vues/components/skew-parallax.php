        <h2>Effet parallax</h2>
        <p>L'image de fond ne défile pas en même temps que le bloc.</p>
        <p>Pour Cela la position du <code>background-image</code> est en <code>fixed</code></p>
        <p>L'effet diagonale du bloc est obtenu grace à une transformation de type <code>SkewY</code>.</p>
        <p>Mais firefox ne semble pas gérer le <code>background-image:fixed</code> et le <code>SkewY</code> ensembles.</p>
        <p>Si vous voulez profiter pleinement de l'effet, je vous déconseille d'utiliser firefox.</p>
